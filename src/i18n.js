function applyTranslations(lang) {
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if (translations[lang] && translations[lang][key]) {
      // If it's a input placeholder, update that
      if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
        el.setAttribute('placeholder', translations[lang][key]);
      } else {
        el.textContent = translations[lang][key];
      }
    }
  });
  
  // Update document language attribute
  document.documentElement.setAttribute("lang", lang);
}

// Check saved language or default to en
let currentLang = localStorage.getItem("bideva_lang") || "en";

document.addEventListener("DOMContentLoaded", () => {
  // Apply language immediately on load
  applyTranslations(currentLang);
  
  // Setup toggle button event listeners
  setupLanguageToggles();
});

function setupLanguageToggles() {
  const toggles = document.querySelectorAll(".lang-toggle-btn");
  toggles.forEach(btn => {
    // Highlight active language button
    const btnLang = btn.getAttribute("data-lang");
    if (btnLang === currentLang) {
      btn.classList.add("active");
    } else {
      btn.classList.remove("active");
    }
    
    btn.addEventListener("click", (e) => {
      e.preventDefault();
      const newLang = btn.getAttribute("data-lang");
      if (newLang !== currentLang) {
        currentLang = newLang;
        localStorage.setItem("bideva_lang", currentLang);
        applyTranslations(currentLang);
        
        // Update visual state of all toggles
        document.querySelectorAll(".lang-toggle-btn").forEach(t => {
          if (t.getAttribute("data-lang") === currentLang) {
            t.classList.add("active");
          } else {
            t.classList.remove("active");
          }
        });
      }
    });
  });
}

// Expose toggle language helper
window.setBidevaLanguage = function(lang) {
  if (lang === 'en' || lang === 'fr') {
    currentLang = lang;
    localStorage.setItem("bideva_lang", currentLang);
    applyTranslations(currentLang);
  }
};
